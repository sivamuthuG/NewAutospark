function loadBuildData(data) {

    const tableBody = document.getElementById('tableData');
    let tableHtml = '';
    for (let index = 0; index < data.length; index++) {
        let obj = data[index];
        tableHtml += '<tr><td>' + (index + 1) + '</td><td>' + obj.buildLable + '</td><td>' + obj.startedBy + '</td><td>' + obj.buildComment + '</td><td>' + obj.buildSize + '</td><td>' + obj.startesAt + '</td><td>' + obj.viewSourceLink + '</td></tr>';
    }

    tableBody.innerHTML = tableHtml;
}

